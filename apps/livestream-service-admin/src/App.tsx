import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { StreamList } from "./stream/StreamList";
import { StreamCreate } from "./stream/StreamCreate";
import { StreamEdit } from "./stream/StreamEdit";
import { StreamShow } from "./stream/StreamShow";
import { PeerConnectionList } from "./peerConnection/PeerConnectionList";
import { PeerConnectionCreate } from "./peerConnection/PeerConnectionCreate";
import { PeerConnectionEdit } from "./peerConnection/PeerConnectionEdit";
import { PeerConnectionShow } from "./peerConnection/PeerConnectionShow";
import { MessageList } from "./message/MessageList";
import { MessageCreate } from "./message/MessageCreate";
import { MessageEdit } from "./message/MessageEdit";
import { MessageShow } from "./message/MessageShow";
import { UserList } from "./user/UserList";
import { UserCreate } from "./user/UserCreate";
import { UserEdit } from "./user/UserEdit";
import { UserShow } from "./user/UserShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"LivestreamService"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="Stream"
          list={StreamList}
          edit={StreamEdit}
          create={StreamCreate}
          show={StreamShow}
        />
        <Resource
          name="PeerConnection"
          list={PeerConnectionList}
          edit={PeerConnectionEdit}
          create={PeerConnectionCreate}
          show={PeerConnectionShow}
        />
        <Resource
          name="Message"
          list={MessageList}
          edit={MessageEdit}
          create={MessageCreate}
          show={MessageShow}
        />
        <Resource
          name="User"
          list={UserList}
          edit={UserEdit}
          create={UserCreate}
          show={UserShow}
        />
      </Admin>
    </div>
  );
};

export default App;
